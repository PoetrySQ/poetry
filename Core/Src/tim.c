//捕获中断
void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
	//TIM17CH1触发捕获中断
	if (htim->Instance==TIM17 && htim->Channel==HAL_TIM_ACTIVE_CHANNEL_1)
	{
		if((captureState&0x80) == 0)
		{
			if(captureState&0x40)
			{
				//再次捕获到上升沿，标记捕获完
				captureState |= 0x80;
				//读取捕获值（计数值）
				captureValue = HAL_TIM_ReadCapturedValue(&htim17, TIM_CHANNEL_1);
				//标记捕获完成 1000 0000
				captureState &= 0x80;
			}
			else
			{
				captureState = 0;
				captureValue = 0;
				escapeNum = 0;
				//初始化计数器
				__HAL_TIM_SetCounter(&htim17, 0);
				//标记捕获到上升沿
				captureState |= 0x40;
			}
		}
	}
}

//溢出中断
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	if(htim->Instance==TIM17)
	{
		if((captureState&0x80) == 0)
		{
			//已经捕获到第1个上升沿
			if(captureState&0x40)
			{
				//捕获次数溢出
				if((escapeNum&0xffff) == 0xffff)
				{
					//强制捕获完成
					captureState |= 0xA0;

					captureValue = 0xFFFF;
				}
				else
				{
					//定时器溢出次数加1
					escapeNum++;
//					printf("escapeNum: %d",escapeNum);
				}
			}
		}
	}
}

//Get the Frequency of Input Signal
//Return float type
float getFreq()
{
	if(captureState&0x80)
	{
		captureState = 0;
		captureTemp = escapeNum * 10000 + captureValue;
		captureFreq = (1/(captureTemp/100000));
		//printf("<Freq>:%.6f\r\n", captureFreq);
	}
	return captureFreq;
}
